package br.ufpe.cin.amadeus.amadeus_web.domain.content_management;

import java.io.Serializable;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;

/**
 * @author armando
 *
 */
@SuppressWarnings("serial")
@Entity
public class Multimedia implements Serializable{
	@Id @GeneratedValue(strategy=GenerationType.AUTO)	
	private int id;

	private String name;

	private String url;

	private String description;

	private int idObjectMultimedia;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public int getIdObjectMultimedia() {
		return idObjectMultimedia;
	}

	public void setIdObjectMultimedia(int idObjectMultimedia) {
		this.idObjectMultimedia = idObjectMultimedia;
	}
}
